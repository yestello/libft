#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	size_t	i;
	size_t	j;
	char	*str;

	i = 0;
	j = 0;

	str = (char*)malloc(sizeof(*s) *  (len + 1));
	if (!str)
		return (NULL);
	while (s[i])
	{
		if (i >= start && j < len)
		{
			str[j] = s[i];
			j++;
		}
		i++;
	}
	str[j] = '\0';
	return (str);
}

int	main(void)
{

	char	str[] = "salut a tous";
	int	s;
	int	l;

	s = 5;
	l = 12;
	printf("%s", ft_substr(str, s, l));
	return (0);
}
