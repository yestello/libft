#include "libft.h"

int	ft_toupper(int c)
{
	if (c >= 'a' && c <= 'z')
		return (c -  32);

	return (c);
}


int	main(void)
{
	int	l;

	l = 97;
	printf("%d", ft_toupper(l));
	return (0);
}
