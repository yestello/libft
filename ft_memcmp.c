#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	const unsigned char	*str1;
	const unsigned char	*str2;
	size_t	i;

	str1 = (unsigned char *)s1;
	str2 = (unsigned char *)s2;
	i = 0;
	while (i < n)
	{
		if (str1[i] != str2[i])
			return (str1[i] - str2[i]);
		i++;
	}
	return (0);
}

int	main(void)
{
	char	a[] = "salut les copains";
	char	b[] = "salut bes copains";
	int	result;
	size_t	n;

	n = 10;
	result = ft_memcmp(a, b, n);
	printf("%d", result);
	return (0);
}
